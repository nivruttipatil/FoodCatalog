import Foundation
import UIKit
struct FoodScreenTheme {
    struct Color {
        static let black = UIColor.black
        static let white = UIColor.white
        static let clear = UIColor.clear
        static let lightWhite = UIColor(white: 1, alpha: 0.3)
        static let darkGray = UIColor.darkGray
    }
    struct Font {
        static let header = UIFont(name: "HelveticaNeue-Light", size: 18.0)
        static let body = UIFont(name: "HelveticaNeue-Light", size: 15.0)
    }
}

